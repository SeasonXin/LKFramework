-- DROP TABLE IF EXISTS `T_SYS_USER`;

CREATE TABLE `T_SYS_USER` (
  `ID` varchar(32) NOT NULL COMMENT '主键',
  `REMARKS` longtext COMMENT '备注',
  `USING_STATUS` varchar(32) NOT NULL COMMENT '在用状态(枚举)',
  `INSERT_LOGIN_ID` varchar(32) DEFAULT NULL COMMENT '新增操作人ID',
  `INSERT_SYSTEM_TAG` varchar(100) NOT NULL COMMENT '新增操作系统标识',
  `INSERT_TIME` varchar(19) NOT NULL COMMENT '新增操作时间(yyyy-MM-dd HH:mm:ss)',
  `UPDATE_LOGIN_ID` varchar(32) DEFAULT NULL COMMENT '更新操作人ID',
  `UPDATE_SYSTEM_TAG` varchar(100) NOT NULL COMMENT '更新操作系统标识',
  `UPDATE_TIME` varchar(19) NOT NULL COMMENT '更新操作时间(yyyy-MM-dd HH:mm:ss)',
  `BIRTHDAY` varchar(10) DEFAULT NULL COMMENT '出生日期(yyyy-MM-dd)',
  `BIRTHPLACE` varchar(32) DEFAULT NULL COMMENT '出生地(字典)',
  `BUS_ID` varchar(50) DEFAULT NULL COMMENT '业务ID',
  `CELLPHONE` varchar(11) DEFAULT NULL COMMENT '手机号码',
  `CHECK_CODE` varchar(32) DEFAULT NULL COMMENT '校验码',
  `DEGREE` varchar(32) DEFAULT NULL COMMENT '学位(字典)',
  `EDUCATION` varchar(32) DEFAULT NULL COMMENT '学历(字典)',
  `EMAIL` varchar(200) DEFAULT NULL COMMENT '电子邮箱',
  `EMAIL1` varchar(200) DEFAULT NULL COMMENT '电子邮箱',
  `EMAIL2` varchar(200) DEFAULT NULL COMMENT '电子邮箱',
  `EMAIL3` varchar(200) DEFAULT NULL COMMENT '电子邮箱',
  `FIELD1` varchar(200) DEFAULT NULL COMMENT '备用字段',
  `FIELD2` varchar(200) DEFAULT NULL COMMENT '备用字段',
  `FIELD3` varchar(200) DEFAULT NULL COMMENT '备用字段',
  `GENDER` varchar(32) DEFAULT NULL COMMENT '性别(枚举)',
  `MARITAL_STATUS` varchar(32) DEFAULT NULL COMMENT '婚姻状态(字典)',
  `NATION` varchar(32) DEFAULT NULL COMMENT '民族(字典)',
  `PHOTO` longtext COMMENT '头像(base64)',
  `TELEPHONE1` varchar(20) DEFAULT NULL COMMENT '电话号码',
  `TELEPHONE2` varchar(20) DEFAULT NULL COMMENT '电话号码',
  `TELEPHONE3` varchar(20) DEFAULT NULL COMMENT '电话号码',
  `USER_CARD` varchar(64) DEFAULT NULL COMMENT '身份证号码',
  `USER_NAME` varchar(50) NOT NULL COMMENT '姓名',
  `AUTHENTICATION` varchar(32) NOT NULL COMMENT '实名认证等级(枚举)',
  PRIMARY KEY (`ID`),
  KEY `I_USER__USING_STATUS` (`USING_STATUS`),
  KEY `I_USER__INSERT_TIME` (`INSERT_TIME`),
  KEY `I_USER__BIRTHDAY` (`BIRTHDAY`),
  KEY `I_USER__BIRTHPLACE` (`BIRTHPLACE`),
  KEY `I_USER__BUS_ID` (`BUS_ID`),
  KEY `I_USER__EMAIL` (`EMAIL`),
  KEY `I_USER__CELLPHONE` (`CELLPHONE`),
  KEY `I_USER__DEGREE` (`DEGREE`),
  KEY `I_USER__EDUCATION` (`EDUCATION`),
  KEY `I_USER__GENDER` (`GENDER`),
  KEY `I_USER__MARITAL_STATUS` (`MARITAL_STATUS`),
  KEY `I_USER__NATION` (`NATION`),
  KEY `I_USER__USER_CARD` (`USER_CARD`),
  KEY `I_USER__USER_NAME` (`USER_NAME`),
  KEY `I_USER__AUTHENTICATION` (`AUTHENTICATION`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='人员表';



